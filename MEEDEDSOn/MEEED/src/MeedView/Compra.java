    private void BtEfetuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtEfetuarActionPerformed
        // TODO add your handling code here:
        String[] info = new String[6];
        info[0] = codigo.getText();
        info[1] = cliente.getText();
        info[2] = data.getText();
        info[3] = qntProd.getName();
        info[4] = precoProd.getText();
        info[5] = prod.getName();
        modelo.addRow(info);
        codigo.setText("");
        cliente.setText("");
        data.setText("");
        precoProd.setText("");

    }//GEN-LAST:event_BtEfetuarActionPerformed

    private void BtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtExcluirActionPerformed
        // TODO add your handling code here:
        int fila = TableProd.getSelectedRow();
        if (fila >= 0) {
            modelo.removeRow(fila);

        } else {
            JOptionPane.showMessageDialog(null, "Selecionar Fila");
        }
    }//GEN-LAST:event_BtExcluirActionPerformed

